CREATE OR REPLACE TRIGGER "DBCH_E_sequence_insert"
BEFORE INSERT ON DBCH_ENVIRONMENT
FOR EACH ROW
WHEN (new.ID IS NULL)
  BEGIN
    SELECT DBCH_ENV_SEQ.NEXTVAL INTO :new.ID FROM dual;
  END;
/

